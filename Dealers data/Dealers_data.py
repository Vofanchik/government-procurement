class DB:
    def __init__(self):
        self.conn = sqlite3.connect('Dealers.db')
        self.c = self.conn.cursor()
        self.c.execute(
            '''CREATE TABLE IF NOT EXISTS Dealers (id integer primary key, description text NOT NULL UNIQUE, 
            UNIQUE ("description") ON CONFLICT IGNORE)''')
        self.conn.commit()

    def insert_data(self, description):
        self.c.execute('''INSERT INTO Dealers(description) VALUES (?)''',
                       (description,))
        self.c.execute('''SELECT * FROM Dealers ORDER BY id DESC''')
        self.new_table_name = str(self.c.fetchone()[0])
        self.conn.commit()

        self.c.execute('''CREATE TABLE IF NOT EXISTS object''' + self.new_table_name +
                       '''(id integer primary key, organization text, email text, phone text, comment text)''')
        self.conn.commit()

    def insert_data_info(self, id_dealer, organization, email, phone, comment):
        self.c.execute('''INSERT INTO object''' + id_dealer + '''(organization, email, phone, comment)
        VALUES (?,?,?,?)''', (organization, email, phone, comment))
        self.conn.commit()

    def delete_table(self,id_obj):
        self.c.execute('''DROP TABLE object''' + id_obj)
        self.conn.commit()

